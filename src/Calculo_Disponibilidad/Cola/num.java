package Calculo_Disponibilidad.Cola;

public class num {
    private int start;
    private int length;
    private int left;
    private int right;

    public num(){

    }

    public void setStart(int start) {
        this.start = start;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public int getStart() {
        return start;
    }

    public int getLength() {
        return length;
    }

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }
}